package sorting;

import java.util.Arrays;

public class Basic {
    public static void main(String[] args) {
        int arr[] = {5,7,3,4,9,0};
        Arrays.sort(arr);
        System.out.println(Arrays.toString(arr));
        char c[]= {'A','e','a','t','Y'};
        Arrays.sort(c);
        System.out.println(Arrays.toString(c));
    }
}
